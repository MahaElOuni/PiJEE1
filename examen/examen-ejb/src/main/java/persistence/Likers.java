package persistence;

public class Likers {
     private int totalUsers;
     private int totalLikers;
     private int totalUnlikers;
	public int getTotalUsers() {
		return totalUsers;
	}
	public void setTotalUsers(int totalUsers) {
		this.totalUsers = totalUsers;
	}
	public int getTotalLikers() {
		return totalLikers;
	}
	public void setTotalLikers(int totalLikers) {
		this.totalLikers = totalLikers;
	}
	public int getTotalUnlikers() {
		return totalUnlikers;
	}
	public void setTotalUnlikers(int totalUnlikers) {
		this.totalUnlikers = totalUnlikers;
	}
     
     
}
